DROP TABLE IF EXISTS MY_ANIMALS;
DROP TABLE IF EXISTS MY_STABLES;

CREATE TABLE MY_STABLES(
    ID LONG AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255),
    MAX_ANIMALS INT
);

CREATE TABLE MY_ANIMALS(
    ID LONG AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255),
    AGE INT,
    MY_STABLES_ID LONG,
    FOREIGN KEY (MY_STABLES_ID) REFERENCES MY_STABLES
);


